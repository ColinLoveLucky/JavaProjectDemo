package com.qf.cobra.pojo;

public class LoanNdesRelation {
	private String appId;
	private String transactionId;
	private String policyCode;
	private String policyResult;
	private String policyNote;
	private String policyTime;
	
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getTransactionId() {
		return transactionId;
	}
	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}
	public String getPolicyCode() {
		return policyCode;
	}
	public void setPolicyCode(String policyCode) {
		this.policyCode = policyCode;
	}
	public String getPolicyResult() {
		return policyResult;
	}
	public void setPolicyResult(String policyResult) {
		this.policyResult = policyResult;
	}
	public String getPolicyNote() {
		return policyNote;
	}
	public void setPolicyNote(String policyNote) {
		this.policyNote = policyNote;
	}
	public String getPolicyTime() {
		return policyTime;
	}
	public void setPolicyTime(String policyTime) {
		this.policyTime = policyTime;
	}
}
